/** Parameter, Arguments, return statement */
/**
 * Parameter
 * ---------
 * in basics words parameters are the variables that are used to pass value into a function or method. they act as a placeholders for the actual value
 * 
 * Arguments:
 * ----------
 * Arguments are the actual values that are passed into a function or method when it is called. those value are assigned to the corresponding
 * parameters define in the function or method declaration.
 * 
 * Return statement
 * ----------------
 * The return statement is used to specify the value that should the function
 * return after it has finished execution.
 * 
 */